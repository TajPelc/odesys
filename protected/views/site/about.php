    <p>The modern world and our way of life are all about the decisions we make. Be it in our personal lives
    or in business, the choices we take can either lead us to prosperity or bring us to ruin. The problem is
    escalated not only by the fact that the number of decisions that we have to make has increased, but
    also by their increase in complexity. We have to take into account many variables and various,
    possibly long-term, effects on our business and life. Unfortunately the human brain cannot possibly
    wrap around this level of complexity and still make an informed decision. This is why we developed a
    web-based decision support system named ODESYS that augments the user's decision-making
    process.</p>

    <p>Decision support systems (DSS) are nothing new. They have been around since the 1960's, with
    theoretical work dating back to the 1950's. Our approach was to apply the basic concepts that have
    been in use for more than 60 years to a web platform, using the latest available technologies in order
    to increase usability and availability.</p>

    <p>We are continuously trying to develop a framework that helps the decision maker to make a better
    decision by seamlessly guiding him through the stages of the decision-making process while trying to
    get him to creatively think about the problem and come out with possible new alternatives and a
    more accurate model of the decision problem.</p>

    <p>This is achieved by taking a user-centric approach to developing our application. ODESYS is asking the
    user questions about the decision problem in a form that is easily understandable. By using the
    answers to build the model, we are simplifying the interaction between the user and the tool.
    Furthermore all the evaluation is done in a similar fashion and the results are displayed in a graphical,
    easily comprehensible form.</p>

    <p>Where ODESYS distances itself from traditional DSS even more is the social component. We utilize
    the power of social networking to increase ones insight into the decision problem being analyzed by
    inviting other people to look at it from an independent or unbiased point of view. Invited participants
    are able to view the user's decision model and alternatives and contribute by commenting on
    alternatives, the model or results. They are also able to suggest new unconsidered alternatives or
    suggest a change in user's preferences. The decision maker gains deeper insight into the problem which allows him to make an informed decision. However, he is still the one to decide and bear the
    consequences.</p>
    <p>ODESYS may be used in the pedagogical process as an introduction to multi-criteria decision support and decision support systems.</p>

    <h2>A brief history</h2>
    <p>The concept of Odesys and alpha version was developed as a part of a diploma thesis by <em>Taj Pelc</em>, titled Web System For Multicriteria Decision Support (Spletni sistem za podporo odločanju) at the <a href="http://www.fov.uni-mb.si/en/" title="Faculty of Organizational Sciences">Faculty of Organizational Sciences</a> (FOV) in Kranj, Slovenia, under the mentorship of professor dr. Vladislav Rajkovič.</p>
    <p>The thesis fell under the context of the course Decision Support Systems and investigated the development of a web-based decision support system. To this end a unique decision support method was developed based upon Abacon and Kepner-Tregoe methods and was implemented using the latest web technologies available.</p>
    <p>The result was Odesys Alpha, a showcase project that proved, that developing a web based DSS offers quite some benefits over traditional software based DSS. Unlike similar software, Odesys required no installation or dependencies, administrator rights and was not limited by the operating system. It was accessible from any web capable device with a modern browser.</p>
    <p>After taking the diploma, an improved, production ready version (Odesys Beta) was started with the help of another student of FOV, <em>Frenk T. Sedmak Nahtigal</em>. In that form Odesys was limited to solving only non-complex decision problems with up to 10 one-level criteria and up to 10 alternatives, and without advanced analysis methods.</p>
    <p>Now Odesys is a production ready DSS with a single goal - helping you decide.</p>
